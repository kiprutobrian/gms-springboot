package cloudit.africa.GMS.Model;

public class DrivePermision {

	private String id;
	private String email;
	private String fileId;
	private String role;
	private String type;
	private String newEmailAdress;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getFileId() {
		return fileId;
	}
	public void setFileId(String fileId) {
		this.fileId = fileId;
	}
	public String getRole() {
		return role;
	}
	public void setRole(String role) {
		this.role = role;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getNewEmailAdress() {
		return newEmailAdress;
	}
	public void setNewEmailAdress(String newEmailAdress) {
		this.newEmailAdress = newEmailAdress;
	}
	

	
}
